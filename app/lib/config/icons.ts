/**
 * Get an icon option by name
 */
export function getIconOption(name: string): IconOption | undefined {
  return ICON_OPTIONS.find((opt) => opt.name === name);
}

/**
 * Get icons by category
 */
export function getIconsByCategory(category: IconCategory): IconOption[] {
  return ICON_OPTIONS.filter((opt) => opt.category === category);
}

